DROP TABLE IF EXISTS careplans_careplan CASCADE;
DROP TABLE IF EXISTS careplans_order CASCADE;
DROP TABLE IF EXISTS careplans_provider CASCADE;
DROP TABLE IF EXISTS careplans_patient CASCADE;

CREATE TABLE IF NOT EXISTS django_migrations (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  app varchar(255) NOT NULL,
  name varchar(255) NOT NULL,
  applied timestamp with time zone NOT NULL
);

DELETE FROM django_migrations
WHERE app = 'careplans';

CREATE TABLE careplans_patient (
  mrn varchar(50) PRIMARY KEY,
  first_name varchar(100) NOT NULL,
  last_name varchar(100) NOT NULL,
  dob date NOT NULL,
  created_at timestamp with time zone NOT NULL,
  updated_at timestamp with time zone NOT NULL
);

CREATE TABLE careplans_provider (
  npi varchar(20) PRIMARY KEY,
  name varchar(150) NOT NULL,
  created_at timestamp with time zone NOT NULL,
  updated_at timestamp with time zone NOT NULL
);

CREATE TABLE careplans_order (
  id uuid PRIMARY KEY,
  patient_mrn varchar(50) NOT NULL REFERENCES careplans_patient(mrn) DEFERRABLE INITIALLY DEFERRED,
  provider_npi varchar(20) NOT NULL REFERENCES careplans_provider(npi) DEFERRABLE INITIALLY DEFERRED,
  medication_name varchar(150) NOT NULL,
  primary_diagnosis varchar(255) NOT NULL,
  additional_diagnoses text NOT NULL,
  medication_history text NOT NULL,
  patient_records text NOT NULL,
  created_at timestamp with time zone NOT NULL,
  updated_at timestamp with time zone NOT NULL
);

CREATE TABLE careplans_careplan (
  id uuid PRIMARY KEY,
  order_id uuid NOT NULL UNIQUE REFERENCES careplans_order(id) DEFERRABLE INITIALLY DEFERRED,
  content text NOT NULL,
  status varchar(20) NOT NULL,
  created_at timestamp with time zone NOT NULL,
  updated_at timestamp with time zone NOT NULL
);

CREATE INDEX careplans_order_patient_mrn_idx ON careplans_order(patient_mrn);
CREATE INDEX careplans_order_provider_npi_idx ON careplans_order(provider_npi);

